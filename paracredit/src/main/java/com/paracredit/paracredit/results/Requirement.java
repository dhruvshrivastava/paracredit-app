package com.paracredit.paracredit.results;

public class Requirement {
    public String requirement1;
    public String requirement2;
    public String requirement3;
    public String requirement4;
    public String requirement5;

    public Requirement(String requirement1, String requirement2, String requirement3, String requirement4, String requirement5) {
        this.requirement1 = requirement1;
        this.requirement2 = requirement2;
        this.requirement3 = requirement3;
        this.requirement4 = requirement4;
        this.requirement5 = requirement5;
    }

    public String getRequirement1() {
        return this.requirement1;
    }
    public String getRequirement2() {
        return this.requirement2;
    }
    public String getRequirement3() {
        return this.requirement3;
    }
    public String getRequirement4() {
        return this.requirement4;
    }
    public String getRequirement5() {
        return this.requirement5;
    }


}
