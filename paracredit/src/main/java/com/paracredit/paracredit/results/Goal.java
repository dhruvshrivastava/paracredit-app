package com.paracredit.paracredit.results;

public class Goal {
    public String goal1;
    public String goal2;
    public String goal3;
    public String goal4;
    public String goal5;

    public Goal(String goal1, String goal2, String goal3, String goal4, String goal5) {
        this.goal1 = goal1;
        this.goal2 = goal2;
        this.goal3 = goal3;
        this.goal4 = goal4;
        this.goal5 = goal5;
    }

    public String getGoal1() {
        return this.goal1;
    }
    public String getGoal2() {
        return this.goal2;
    }
    public String getGoal3() {
        return this.goal3;
    }
    public String getGoal4() {
        return this.goal4;
    }
    public String getGoal5() {
        return this.goal5;
    }


}
